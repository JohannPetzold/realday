import Foundation

extension CGFloat {
    
    /// `Design System` CGFloat values.
    public enum DesignSystem {
        
        /// Design System `Spacing` values.
        public enum Spacing {
            
            /// Design System `xxs` spacing: 2px.
            public static let xxs: CGFloat = 2
            
            /// Design System `xs` spacing: 4px.
            public static let xs: CGFloat = 4
            
            /// Design System `s` spacing: 8px.
            public static let s: CGFloat = 8
            
            /// Design System `m` spacing: 12px.
            public static let m: CGFloat = 12
            
            /// Design System `l` spacing: 16px.
            public static let l: CGFloat = 16
            
            /// Design System `xl` spacing: 24px.
            public static let xl: CGFloat = 24
            
            /// Design System `xxl` spacing: 32px.
            public static let xxl: CGFloat = 32
            
            /// Design System `xxxl` spacing: 48px.
            public static let xxxl: CGFloat = 48
            
            /// Design System `xxxxl` spacing: 64px.
            public static let xxxxl: CGFloat = 64
        }
    }
}
